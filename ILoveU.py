import turtle

def curve(n):
    for i in range(n):
        t.forward(2)
        t.right(3)

t= turtle.Turtle()
t.pensize(3)
t.shape("turtle")
t.speed(1)
turtle.bgcolor('red')
t.pencolor('black')

# drawing I 
t.penup()
t.goto(-200,0)
t.pendown()
t.fillcolor("black")
t.begin_fill()
t.forward(100)
t.left(90)
t.forward(30)
t.left(90)
t.forward(100)
t.left(90)
t.forward(30)
t.end_fill()
t.begin_fill()

t.penup()
t.goto(-135,30)
t.pendown()
t.setheading(90)
t.forward(100)
t.left(90)
t.forward(30)
t.left(90)
t.forward(100)
t.end_fill()
t.begin_fill()

t.penup()
t.goto(-200,130)
t.pendown()
t.setheading(0)
t.forward(100)
t.left(90)
t.forward(30)
t.left(90)
t.forward(100)
t.left(90)
t.forward(30)
t.end_fill()
# I is drawn 

# drawing heart
t.penup()
t.goto(0,10)
t.pendown()
t.setheading(0)
t.fillcolor("black")
t.begin_fill()
t.left(130)
t.forward(100)
curve(75)
t.left(189)
curve(73)
t.right(5)
t.forward(108)
t.end_fill()
# heart is drawn

# drawing U 
t.penup()
t.goto(198,160)
t.pendown()
t.setheading(270)
t.pensize(30)
t.forward(105)
curve(60)
t.forward(105)
t.hideturtle()
turtle.mainloop()  